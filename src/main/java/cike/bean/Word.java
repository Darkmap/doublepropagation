package cike.bean;

public class Word {
	
	private String txt;
	private String pos;
	
	
	
	
	
	public Word(String txt, String pos) {
		super();
		this.txt = txt;
		this.pos = pos;
	}





	public String getTxt() {
		return txt;
	}





	public void setTxt(String txt) {
		this.txt = txt;
	}





	public String getPos() {
		return pos;
	}





	public void setPos(String pos) {
		this.pos = pos;
	}
	
	
	
	
	
}
